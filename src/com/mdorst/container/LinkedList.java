public class LinkedList<T> extends LinkedListBase<T> {
    @Override
    public void pushFront(T obj) {
        super.pushFront(obj);
    }
    @Override
    public void pushBack(T obj) {
        super.pushBack(obj);
    }
    @Override
    public T popFront() {
        return super.popFront();
    }
    @Override
    public T popBack() {
        return super.popBack();
    }
}
